let firstNum = parseInt(prompt('Введите первое число', 5));
let secondNum = parseInt(prompt('Введите второе число', 2));

let sum = firstNum + secondNum;
let diff = firstNum - secondNum;
let multip = firstNum * secondNum;
let division = firstNum / secondNum;

console.log('Сумма чисел равно ' + sum);
console.log('Разница чисел равно ' + diff);
console.log('Умножение чисел равно ' + multip);
console.log('Деление чисел равно ' + division);